package  utils;
public class isPositive {
    public static boolean positiveNumber(int n) {
        if (n < 0 ) {
            return false;
        }

        else {
            return true;
        }
    }
    public static boolean positiveNumber(double n) {
        if (n < 0) {
            return false;
        }

        else {
            return true;
        }
    }
}